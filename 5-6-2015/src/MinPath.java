
public class MinPath {
	private int start;
	private int end;
	private int cost;
	
	public MinPath() {
		this.start=0;
		this.end=0;
		this.cost=0;
	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}
	
	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}

	public int getCost() {
		return cost;
	}

	public void setCost(int cost) {
		this.cost = cost;
	}
	

}
